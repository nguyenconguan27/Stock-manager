package com.manager.stock.manager_stock.model;

public class ProductGroup {
    private String id;
    private String name;

    public ProductGroup(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
